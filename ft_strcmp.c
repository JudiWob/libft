#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int i;

	i = 0;
	while (s1[i] && s2[i] && (s1[i] == s2[i]))
		i++;
	return (s1[i] - s2[i]);
}

int main()
{
	char *s1 = "Hello";

	char *s2 = "Hello";

	printf("result: %i", ft_strcmp(s1, s2));
}